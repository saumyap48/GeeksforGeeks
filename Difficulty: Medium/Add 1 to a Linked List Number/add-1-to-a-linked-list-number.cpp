class Solution {
public:
    Node* reverseList(Node* head){
        Node* prev=NULL;
        Node* current=head;
        Node* next=NULL;
        while(current!=NULL){
            next=current->next;
            current->next=prev;
            prev=current;
            current=next;
        }
        return prev;
    }
    Node *addOne(Node *head) {
       head=reverseList(head);
       Node* current=head;
       int carry=1;
       while(current!=NULL){
        int sum=current->data+carry;
        carry=sum/10;
        current->data=sum%10;
        if(carry==0){
            break;
        }
        if(current->next==NULL && carry!=0){
            current->next=new Node(carry);
            break;
        }
        current=current->next;
       }
       head=reverseList(head);
       return head;
    }
};


