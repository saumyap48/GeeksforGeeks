class Solution {
  public:
    vector<int> nthRowOfPascalTriangle(int n) {
        vector<int>answer;
        int coef=1;
            for(int j=1;j<=n;j++)
            {
                answer.push_back(coef);
                coef=coef*(n-j)/j;
            }
        return answer;
    }
};



//{ Driver Code Starts.

void printAns(vector<int> &ans) {
    for (auto &x : ans) {
        cout << x << " ";
    }
    cout << "\n";
}

int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        Solution ob;
        auto ans = ob.nthRowOfPascalTriangle(n);
        printAns(ans);
    }
    return 0;
}

