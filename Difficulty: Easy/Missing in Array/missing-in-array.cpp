class Solution {
  public:
    int missingNumber(vector<int>& arr) {
    int n=arr.size();
    int sum1=0,sum=0;
     sum=((n + 1) * (n + 2)) / 2;
    for(int i=0; i<n; i++){
         sum1=sum1+arr[i];
    }
    return sum-sum1;
    }
};

